package ej2;

public class ArgsDouble {

	
	
	//ej2
	
	//a)
	/**
	 * funcion que devuelve la media de 2 numeros int
	 * @param n1
	 * @param n2
	 * @return
	 */
	static double mediaArgsDouble(int n1,int n2) {
		double media;
		
		
		media=n1*n2/2;
		return media;
	}
	

	//b)
	/**
	 * funcion que devuelve la media de 3 numeros int
	 * @param n1
	 * @param n2
	 * @return
	 */
	static double mediaArgsDouble(int n1,int n2,int n3) {
		double media;
		
		
		media=n1*n2*n3/3;
		return media;
	}
	
	
	
}
